                <h3 style="margin-top: 0">Translate Inception Theme</h3>
                <p>Translate Inception Theme is so easy. By defaultt the themes include two languages: <em>English</em> and <em>Spanish</em>,
                    however you can translate it into your own language in a very simple way with the use of <strong><a href="http://www.poedit.net/" target="_blank">PoEdit</a></strong>
                    or any other similar software that works with <strong><em>po</em></strong> files.</p>

                <p>Once you have the program, locate the folder "<em>lang</em>" in Inception theme. You can see <em>mo</em> and <em>po</em> files.
                    Duplicate the file named <em>en.po</em>, rename it with the name of your language(eg br.po) and open it with PoEdit.</p>

                <img src="<?php echo XOOPS_THEME_URL; ?>/inception/images/poedit.jpg" alt="PoEdit" class="img-responsive" />

                <p>Now you will only translate each line into your own language. Once you have finished save the file. You will see that a new
                    file has been created "mo" with the name of the po file edited.</p>

                <p>To activate the language, go to the XOOPs control panel, then foto to Common Utilities preferences and select the new language.</p>

                <p><strong>Advise:</strong> In order to select the new language, Common Utilities must have this language available in "rmcommon/lang" folder, otherwise
                    this language will not available.</p>
