    <h2>Traducir Inception Theme</h2>
    <p>Traducir Inception Theme es muy sencillo. Por defecto el tema incluye dos idiomas: <em>Inglés</em> y <em>Español</em>, sin embargo tu puedes traducirlo a 
    tu propio idioma de forma muy simple con el uso de <strong><a href="http://www.poedit.net/" target="_blank">PoEdit</a></strong> u otro editor similar que 
    trabaje con ficheros <strong><em>po</em></strong>.</p>
    
    <p>Una vez cuentes con el programa, localiza la carpeta "<em>lang</em>" dentro del tema Inception. Podrás ver que existen archivos <em>mo</em> y archivos <em>po</em>.
    Duplica el archivo llamado <em>en.po</em>, renombrálo con el nombre de tu idioma (ej. br.po) y abrelo con PoEdit.</p>
    
    <img src="<?php echo XOOPS_THEME_URL; ?>/inception/images/poedit.jpg" alt="PoEdit" />
    
    <p>Ahora solo deberás traducir cada línea a tu propio idioma. Cuando hayas terminado guarda el archivo. Podrás observar que se ha creado un nuevo archivo "mo" con el nombre
    del archivo po editado.</p>
    
    <p>Para activar el idioma, ve al panel de control de XOOPS. Ingresa a las opciones de Common Utilities y selecciona el nuevo idioma,</p>
    
    <p><strong>Importante:</strong> Para poder seleccionar el nuevo idioma, Common Utilities debe contar con el mismo idioma, de lo contrario no se podrá elegir el archivo.</p>
</div>